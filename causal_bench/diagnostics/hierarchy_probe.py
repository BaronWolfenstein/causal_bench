"""Diffusion phase-transition probe for hierarchical/compositional structure (#122).

Sclocchi-Favero-Wyart, "A Phase Transition in Diffusion Models Reveals the
Hierarchical Nature of Data" (and the Random Hierarchy Model): if data is
generated by a latent tree, forward diffusion destroys **fine** (sub-class)
identity at a *lower* noise level than the **coarse** (class) identity — two
separated transitions. A single-scale (flat) distribution has one transition.
The separation is the signature of hierarchical structure, and equals a
belief-propagation reconstruction threshold on the latent tree.

This CPU prototype measures the separation analytically: at each VP-SDE noise
level, classify the noised samples to the nearest (scaled) class mean — the exact
Bayes MAP for an isotropic Gaussian mixture, i.e. the inference (BP) view of
denoising — and track coarse- vs fine-class accuracy vs noise. The noise level at
which each accuracy erodes to chance is that scale's transition ``t*``; a
hierarchical dataset has ``t*_fine < t*_coarse``.

Reuses `generative/vpsde` for the noise schedule. numpy only. The trained-score-net
and real-MEDS versions (and the BP-on-inferred-tree prediction of t*) are the
follow-ups in #122; this establishes the signature on synthetic tree data.
"""
from __future__ import annotations

import numpy as np

from causal_bench.generative.vpsde import Schedule, alpha_bar


def sample_hierarchical_gaussian(*, n_coarse: int = 4, n_fine: int = 3,
                                 per_leaf: int = 60, dim: int = 8,
                                 coarse_sep: float = 2.5, fine_sep: float = 0.7,
                                 sigma_within: float = 0.3, seed: int = 0) -> dict:
    """Two-level Gaussian tree: ``n_coarse`` classes separated by ``coarse_sep``,
    each split into ``n_fine`` sub-classes separated by ``fine_sep`` (``fine_sep <
    coarse_sep`` ⇒ a real hierarchy of scales), leaves ~ ``N(fine_mean, σ_within²)``.
    Returns ``X`` and integer ``coarse`` / ``fine`` labels plus the
    ``coarse_means`` (``n_coarse``) and ``fine_means`` (``n_coarse·n_fine``)."""
    rng = np.random.default_rng(seed)
    coarse_dirs = rng.normal(size=(n_coarse, dim))
    coarse_dirs /= np.linalg.norm(coarse_dirs, axis=1, keepdims=True)
    coarse_means = coarse_sep * coarse_dirs
    X, coarse, fine, fine_means = [], [], [], []
    for c in range(n_coarse):
        for f in range(n_fine):
            if n_fine == 1:
                offs = np.zeros(dim)                       # single subclass = the coarse class (flat)
            else:
                offs = rng.normal(size=dim)
                offs = fine_sep * offs / (np.linalg.norm(offs) + 1e-12)
            fmean = coarse_means[c] + offs
            fine_means.append(fmean)
            fidx = c * n_fine + f
            X.append(fmean + sigma_within * rng.normal(size=(per_leaf, dim)))
            coarse.extend([c] * per_leaf)
            fine.extend([fidx] * per_leaf)
    return {"X": np.vstack(X), "coarse": np.asarray(coarse), "fine": np.asarray(fine),
            "coarse_means": coarse_means, "fine_means": np.asarray(fine_means)}


def _map_accuracy(Xt: np.ndarray, scaled_means: np.ndarray, labels: np.ndarray) -> float:
    """MAP class accuracy: assign each noised point to the nearest scaled mean
    (exact Bayes for an isotropic Gaussian mixture — variance is common across
    classes, so the posterior reduces to nearest-mean)."""
    d2 = ((Xt[:, None, :] - scaled_means[None, :, :]) ** 2).sum(-1)   # (n, K)
    return float((d2.argmin(1) == labels).mean())


def estimate_transition_t(t_frac: np.ndarray, acc: np.ndarray, *, chance: float) -> float:
    """The transition ``t*`` (as a schedule fraction): the first noise level at
    which accuracy erodes to the halfway point between perfect and chance. Returns
    1.0 if it never erodes that far (identity survives to the end)."""
    t_frac, acc = np.asarray(t_frac, float), np.asarray(acc, float)
    thresh = 0.5 * (acc[0] + chance)
    below = np.where(acc <= thresh)[0]
    return float(t_frac[below[0]]) if below.size else 1.0


def phase_transition_scan(X: np.ndarray, coarse: np.ndarray, fine: np.ndarray,
                          coarse_means: np.ndarray, fine_means: np.ndarray, *,
                          sch: Schedule | None = None, n_grid: int = 25,
                          rng: np.random.Generator | None = None) -> dict:
    """Scan VP-SDE noise levels; at each, noise ``X`` and record coarse- and
    fine-class MAP accuracy. Returns ``t_frac``, ``acc_coarse``, ``acc_fine`` and
    the transition fractions ``t_coarse_star`` / ``t_fine_star``. Hierarchical
    data ⇒ ``t_fine_star < t_coarse_star`` (fine identity lost first)."""
    sch = sch or Schedule(n_steps=200)
    rng = rng or np.random.default_rng(0)
    X = np.asarray(X, float)
    steps = np.linspace(1, sch.n_steps - 1, n_grid).astype(int)
    t_frac = steps / sch.n_steps
    acc_c, acc_f = [], []
    for t in steps:
        a = alpha_bar(sch, int(t))
        Xt = np.sqrt(a) * X + np.sqrt(1.0 - a) * rng.normal(size=X.shape)
        acc_c.append(_map_accuracy(Xt, np.sqrt(a) * coarse_means, coarse))
        acc_f.append(_map_accuracy(Xt, np.sqrt(a) * fine_means, fine))
    acc_c, acc_f = np.asarray(acc_c), np.asarray(acc_f)
    chance_c = 1.0 / len(coarse_means)
    chance_f = 1.0 / len(fine_means)
    return {
        "t_frac": t_frac, "acc_coarse": acc_c, "acc_fine": acc_f,
        "t_coarse_star": estimate_transition_t(t_frac, acc_c, chance=chance_c),
        "t_fine_star": estimate_transition_t(t_frac, acc_f, chance=chance_f),
    }
